package House.rooms;

public class RestRoom {
    private boolean showerCabin;
    private int width;
    private int height;
    private String color;

    public boolean isShowerCabin() {
        return showerCabin;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public String getColor() {
        return color;
    }
}
